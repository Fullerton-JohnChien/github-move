								<span>令人難忘的古早味</span>三官大帝廟旁肉粽</h3>
							<p>如果你厭煩了觀光客的嘈雜，暫時不想再聽到相機快門聲不絕於耳，只願悠哉地在路邊吃一頓道地的台南小吃當早餐，這時，三官大帝廟旁肉粽會是你的好選擇。</p>
							<p>這也是一家沒有特別取名字的小店，大概是因為營業位置就是住家門口吧，開始的時候只是要做給附近鄰居吃的，所以爐火和簡單的座位都設在等於自家前院一樣的地方，該知道地方的人自然會知道啊，店名就變得非常不重要。</p>
							<p>小攤主要提供與虱目魚有關的各種湯品，譬如魚皮湯與魚肚湯，也可以做成魚皮粥或魚肚粥，但其實就是魚湯加飯做成的鹹粥，有肉、有飯、有熱湯，是很台南的早餐。虱目魚處理得很乾淨，不太需要擔心多刺的問題，魚肉肉質緊實，魚肚又肥美嫩滑，而且量好多啊，簡直是吃肉配湯，很過癮。也可以加點一根油條來蘸湯吃，吸附魚骨高湯的油條，又鮮又脆，口感大加分。</p>
							<p>在這裡吃虱目魚湯，除了油條還要配肉粽。三官大帝廟旁肉粽有著近似迴力鏢的特殊外型，不論從哪個角度看來都非常美觀， 動筷子前先欣賞一下老闆的手藝是一定要的。</p>
							<p>至於餡料方面則是走樸實路線，肥肉瘦肉分布均勻，米粒柔軟但仍保有彈性，味道是傳統的古早味，也許不夠出乎意料，但配上店家自製微辣的沾醬食用， 口味也真是一絕了，一不小心就要吃掉半罐！</p>
