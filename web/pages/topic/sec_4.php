								<img class="door" src="/web/pages/topic/img/topic4/doorplate2.jpg">
								<span>五點起床才吃得到的</span>六千牛肉湯</h3>
							<p>六千牛肉湯的店名來自「六味之內有合一人情，千好萬法為請誰保重」，是王爺給的籤詩。</p>
							<p>「台南最具代表性的早餐」，是說，假設有這項排名的話啦。假如要（很無聊地）為台南特殊的早餐文化做出先後排名，六千牛肉湯大概是不作第二人想的第一名。</p>
							<p>早上五點開賣的早餐，對於更習慣早午餐的都市人來說，真是太難想像了。「誰會天沒亮就出門只為吃肉湯呢？」追求睡到自然醒的都市人實在無法理解。不過還真的就是有那麼多人會為了六千牛肉湯而早早出門，所以常常才過八點就賣完了。當都市人才準備出門上班，六千牛肉湯的一天已近尾聲。三小時完售的六千牛肉湯的魅力到底在哪裡呢？</p>
							<p>看作法的話也真夠簡單了，切片生肉鋪進碗底，差不多堆到半碗多的高度吧，然後湯瓢一舀，把以牛大骨熬成的熱湯淋在生牛肉上，再灑上一點點鹽，就上桌了。</p>
							<p>「這就是九點之後保證吃不到的秒殺美食？」賣相很一般嘛。看旁邊的台南人都把牛肉湯當菜似的、牛肉就蘸油膏配白飯吃，於是依樣畫葫蘆也這樣吃， 才發現， 哇哇哇！這湯也太鮮甜了吧，玫瑰色的牛肉有點熟又不會太熟，嫩度剛剛好，而且甜味一滴也不流失地全留在碗裡，所以越吃越滿足喔。</p>
							<p>尤其清甜的肉湯喝不夠還可以加湯，這種好事大概也只有在台南才會發生吧？忽然覺得有牛肉湯當早餐的台南人還真幸福，早起的人兒有肉吃喔！</p>
